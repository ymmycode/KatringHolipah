    void ProcessInput() throws SQLException {
        if ("".equals(jTextField1.getText()) || "".equals(jTextField2.getText())|| "".equals(jTextArea1.getText()))
        {JOptionPane.showMessageDialog(null , "Isi Form dengan benar");}
        else
        {
            String nama = jTextField1.getText();
            String telp = jTextField2.getText();
            String alamat = jTextArea1.getText();
            

            sql1 = "INSERT INTO pelanggan(nama, telp, alamat) VALUES ('"
                +nama+"','"
                +telp+"','"
                +alamat+"')";

            stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql1);
            stat.execute(sql1);
            stat.close();
        }
        
        
    }
    
